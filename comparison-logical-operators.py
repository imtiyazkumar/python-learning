# Logical Operators
# or -> (atleast one is true)
# and -> (both are true)
# not -> (reverses any value)
number = 2
print(number > 3)
print(number < 3)
print(not number > 3)
print(not number < 3)
print(number > 3 and number > 1)
print(number > 3 or number > 1)
